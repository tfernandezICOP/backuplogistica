 private void cargarModelosYTipos() {
    List<Marca> marcas = controladoraMarca.obtenerTodasLasMarcas();

    jComboBox1.removeAllItems();
    jComboBox2.removeAllItems();

    Set<String> modelosUnicos = new HashSet<>();
    Set<String> tiposUnicos = new HashSet<>();

    for (Marca marca : marcas) {
        modelosUnicos.add(marca.getModelo());
        tiposUnicos.add(marca.getTipo());
    }

    modelosUnicos.forEach(jComboBox2::addItem);
    tiposUnicos.forEach(jComboBox1::addItem);

    // Seleccionar automáticamente el modelo y tipo del vehículo seleccionado
    jComboBox2.setSelectedItem(vehiculo.getMarca().getModelo());
    jComboBox1.setSelectedItem(vehiculo.getMarca().getTipo());
}

